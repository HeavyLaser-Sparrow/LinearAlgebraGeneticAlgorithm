def select_parents(population):
  """Selects the top 50% of the population based on score."""
  population.sort(key=lambda ship: ship.getScore(), reverse=True)
  num_parents = (len(population) + 1) // 2  # Round up for odd sizes
  return population[:num_parents]

def crossover(parent1, parent2, base_ships, base_outfits, weapon_outfits, thruster_outfits, turn_outfits):
  """Combines two parents to create a child ship."""
  # Randomly choose base ship from one of the parents
  chosen_base_ship_name = random.choice([parent1.getName(), parent2.getName()])
  newShipName, newShipVector = chosen_base_ship_name, numpy.array(base_ships[chosen_base_ship_name])

  childOutfits = ["Hyperdrive"]
  newShipVector += numpy.array(base_outfits["Hyperdrive"])

  # Combine outfits from parents, adding only if valid
  all_parent_outfits = list(set(parent1.getOutfits() + parent2.getOutfits()))
  random.shuffle(all_parent_outfits) # Randomize order to prevent bias

  for outfit_name in all_parent_outfits:
    if outfit_name not in childOutfits and outfit_name != "Hyperdrive":
      testShipVector = newShipVector + numpy.array(base_outfits[outfit_name])
      if is_vector_valid(testShipVector):
        newShipVector = testShipVector
        childOutfits.append(outfit_name)

  # Ensure at least one weapon, thruster, and turn outfit
  for outfit_type_list, outfit_type_name in [
      (weapon_outfits, "weapon"),
      (thruster_outfits, "thruster"),
      (turn_outfits, "turn")
  ]:
    has_type = False
    for outfit in childOutfits:
      if outfit in outfit_type_list:
        has_type = True
        break

    if not has_type:
      # Attempt to add a missing type with limited tries
      tries = 0
      max_tries = 20
      while not has_type and tries < max_tries:
        random_outfit = random.choice(outfit_type_list)
        if random_outfit not in childOutfits:
          testShipVector = newShipVector + numpy.array(base_outfits[random_outfit])
          if is_vector_valid(testShipVector):
            newShipVector = testShipVector
            childOutfits.append(random_outfit)
            has_type = True
        tries += 1

  child_ship = Ship(newShipName, childOutfits, newShipVector)
  return child_ship

def mutate(ship, base_outfits, weapon_outfits, thruster_outfits, turn_outfits):
  """Randomly adds or removes an outfit from a ship."""
  mutated_ship = Ship(ship.getName(), list(ship.getOutfits()), numpy.array(ship.getVector()))
  current_outfits = list(mutated_ship.getOutfits())
  current_vector = numpy.array(mutated_ship.getVector())

  action = random.choice(["add", "remove"])

  if action == "add":
    available_outfits = [o for o in base_outfits.keys() if o not in current_outfits]
    if available_outfits:
      outfit_to_add = random.choice(available_outfits)
      testShipVector = current_vector + numpy.array(base_outfits[outfit_to_add])
      if is_vector_valid(testShipVector):
        mutated_ship.outfits.append(outfit_to_add)
        mutated_ship.vector = testShipVector

  elif action == "remove":
    removable_outfits = [o for o in current_outfits if o != "Hyperdrive"]

    if removable_outfits:
      random.shuffle(removable_outfits) # Randomize selection for removal
      for outfit_to_remove in removable_outfits:
        testShipVector = current_vector - numpy.array(base_outfits[outfit_to_remove])

        # Check if removing would leave the ship without required outfits
        temp_outfits = [o for o in current_outfits if o != outfit_to_remove]
        has_weapon_after_remove = any(o in weapon_outfits for o in temp_outfits)
        has_thruster_after_remove = any(o in thruster_outfits for o in temp_outfits)
        has_turn_after_remove = any(o in turn_outfits for o in temp_outfits)

        if (is_vector_valid(testShipVector) and
            has_weapon_after_remove and
            has_thruster_after_remove and
            has_turn_after_remove):

          mutated_ship.outfits.remove(outfit_to_remove)
          mutated_ship.vector = testShipVector
          break # Successfully removed one outfit, exit loop

  # Re-calculate score, maxSpeed, acceleration, and turn for the mutated ship
  # This is important because the vector changed and these are derived attributes
  mutated_ship.turn = mutated_ship.vector[35] / mutated_ship.vector[6] # thrust/drag
  mutated_ship.maxSpeed = mutated_ship.vector[35] / mutated_ship.vector[7] # thrust/mass
  mutated_ship.acceleration = mutated_ship.vector[34] / mutated_ship.vector[6]
  vectorNorm = numpy.linalg.norm(numpy.array(mutated_ship.vector)) #norm of vector ||v||
  if vectorNorm != 0:
    normalVector = numpy.array(mutated_ship.vector) / vectorNorm #normalized vector v/||v||
    mutated_ship.score = numpy.dot(normalVector, scoreIndex)
  else:
    mutated_ship.score = 0.0

  return mutated_ship

